package src;

public class Calculator {
    public double sum (double num1, double num2){
        return num1 + num2;
    }
    public double multiply(double num1, double num2){
        return num1 * num2;
    }

    public double sub (double num1, double num2){
        return num1 - num2;
    }
    public double divide (double num1, double num2){
        return num1 / num2;
    }
}
